package step03_inheritance.tv1;

public class LgTv{
	public void turnOn(){
		System.out.println("LG TV-전원을 켠다");
	}
	public void turnOff(){
		System.out.println("LG TV-전원을 끈다");
	}
	public void soundUp(){
		System.out.println("LG TV-소리를 높힌다");
	}
	public void soundDown(){
		System.out.println("LG TV-소리를 줄인다");
	}
}